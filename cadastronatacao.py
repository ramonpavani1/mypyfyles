if float(idade_aluno) >=5 and float(idade_aluno) <=7:
    print("Aluno esta na categoria Infantil A.")
elif float(idade_aluno) >=8 and float(idade_aluno) <=10:
    print("Aluno esta na categoria Infantil B.")
elif float(idade_aluno) >=11 and float(idade_aluno) <=13:
    print("Aluno esta na categoria Juvenil  A.")
elif float(idade_aluno) >=14 and float(idade_aluno) <=17:
    print("Aluno esta na categoria Juvenil B.")
elif float(idade_aluno) >= 18:
    print("Categoria Adulto")
    



